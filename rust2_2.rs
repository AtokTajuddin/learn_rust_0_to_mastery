// Belajar datatype dasar

fn main(){
    let angka_integer: i32 = -10;
    let angka_unsigned: u32 = 100;
    let angka_float: f64 = 3.14;
    let var_bool : bool = true;
    let karakter: char = 'A';

    println!("Angka integer: {}", angka_integer);
    println!("Angka unsigned: {}", angka_unsigned);
    println!("Angka float: {}", angka_float);
    println!("Variabel boolean: {}", var_bool);
    println!("Karakter: {}", karakter);
}